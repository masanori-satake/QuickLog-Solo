import os
import re
import sys

# Re-use forbidden patterns for consistency
FORBIDDEN_PATTERNS = [
    r'\bfetch\b',
    r'\bXMLHttpRequest\b',
    r'\bIndexedDB\b',
    r'eval\s*\(',
    r'new\s+Function\s*\(',
    r'\blocalStorage\b',
    r'\bsessionStorage\b',
    r'\bcookie\b',
    r'\bBroadcastChannel\b',
    r'\bWebSocket\b'
]

def validate_metadata(content, filename):
    """
    Validates metadata in the animation module content.
    Returns True if valid, False otherwise.
    """
    metadata_match = re.search(r'static\s+metadata\s*=\s*(\{[\s\S]*?\});', content)
    if not metadata_match:
        return True # Or False if mandatory

    metadata_text = metadata_match.group(1)

    # Check for HTML tags
    if '<' in metadata_text or '>' in metadata_text:
        print(f"Error in {filename}: Potential HTML tags in metadata.")
        return False

    # Check for forbidden patterns
    for pattern in FORBIDDEN_PATTERNS:
        if re.search(pattern, metadata_text):
            print(f"Error in {filename}: Forbidden pattern '{pattern}' in metadata.")
            return False

    return True

def generate_registry():
    animation_dir = 'src/js/animation'
    registry_file = 'src/js/animation_registry.js'

    if not os.path.exists(animation_dir):
        print(f"Directory {animation_dir} not found.")
        return

    files = [f for f in os.listdir(animation_dir) if f.endswith('.js')]
    files.sort()

    imports = []
    entries = []
    has_error = False

    for filename in files:
        filepath = os.path.join(animation_dir, filename)
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()

        if not validate_metadata(content, filename):
            has_error = True
            continue

        # Extract class name from "export default class ClassName"
        match = re.search(r'export\s+default\s+class\s+(\w+)', content)
        if match:
            class_name = match.group(1)
            module_id = filename[:-3]
            alias = class_name

            imports.append(f"import {alias} from './animation/{filename}';")
            entries.append(f"    {{ id: '{module_id}', class: {alias}, metadata: {alias}.metadata }}")

    if has_error:
        print("Registry generation failed due to metadata validation errors.")
        sys.exit(1)

    registry_content = f"""// This file is auto-generated by scripts/generate_animation_registry.py
// Do not edit this file manually.

{chr(10).join(imports)}

export const animations = [
{(',' + chr(10)).join(entries)}
];
"""

    with open(registry_file, 'w', encoding='utf-8') as f:
        f.write(registry_content)

    print(f"Generated {registry_file} with {len(entries)} animations.")

if __name__ == "__main__":
    generate_registry()
